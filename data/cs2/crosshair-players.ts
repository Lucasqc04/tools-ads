const computeEdpi = (sensitivity?: number, dpi?: number): number | undefined => {
  if (typeof sensitivity !== 'number' || typeof dpi !== 'number') {
    return undefined;
  }

  return Number((sensitivity * dpi).toFixed(2));
};

export const cs2CrosshairPlayers: Cs2PlayerCrosshair[] = [...seedPlayers]
  .map((player) => ({
    ...player,
    edpi: computeEdpi(player.sensitivity, player.dpi),
  }))
  .sort((a, b) => a.displayName.localeCompare(b.displayName));

export const getCs2PlayerBySlug = (slug: string): Cs2PlayerCrosshair | undefined => {
  const normalized = slug.trim().toLowerCase();

  return cs2CrosshairPlayers.find((player) => {
    if (player.slug.toLowerCase() === normalized) {
      return true;
    }

    return player.aliases.some((alias) => alias.toLowerCase() === normalized);
  });
};
