import type { AppLocale } from '@/lib/i18n/config';

export type Cs2CommandCategory =
  | 'practice'
  | 'round'
  | 'money'
  | 'grenades'
  | 'smokes'
  | 'bots'
  | 'radar'
  | 'hud'
  | 'viewmodel'
  | 'fps'
  | 'audio'
  | 'binds'
  | 'autoexec'
  | 'competitive'
  | 'fun'
  | 'server'
  | 'dangerous_or_not_recommended';

export type Cs2Command = {
  id: string;
  command: string;
  category: Cs2CommandCategory;
  requiresSvCheats: boolean;
  localOnly: boolean;
  safeForOfficialServers: boolean;
  showInCompetitiveConfig: boolean;
  showInPracticeConfig: boolean;
  showInFunConfig: boolean;
  showInTournamentSafeConfig: boolean;
  needsValidation?: boolean;
  warningLevel: 'none' | 'info' | 'caution' | 'danger';
  locales: {
    'pt-br': {
      name: string;
      description: string;
      exampleUse?: string;
      warning?: string;
    };
    en: {
      name: string;
      description: string;
      exampleUse?: string;
      warning?: string;
    };
    es: {
      name: string;
      description: string;
      exampleUse?: string;
      warning?: string;
    };
  };
};

type CommandLocaleCopy = Cs2Command['locales'];

type CommandSeed = Omit<Cs2Command, 'locales'> & {
  name: Record<AppLocale, string>;
  description: Record<AppLocale, string>;
  exampleUse?: Record<AppLocale, string>;
  warning?: Record<AppLocale, string>;
};

