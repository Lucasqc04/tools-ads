const localCopy = (seed: CommandSeed): CommandLocaleCopy => ({
  'pt-br': {
    name: seed.name['pt-br'],
    description: seed.description['pt-br'],
    exampleUse: seed.exampleUse?.['pt-br'],
    warning: seed.warning?.['pt-br'],
  },
  en: {
    name: seed.name.en,
    description: seed.description.en,
    exampleUse: seed.exampleUse?.en,
    warning: seed.warning?.en,
  },
  es: {
    name: seed.name.es,
    description: seed.description.es,
    exampleUse: seed.exampleUse?.es,
    warning: seed.warning?.es,
  },
});

const cmd = (seed: CommandSeed): Cs2Command => ({
  id: seed.id,
  command: seed.command,
  category: seed.category,
  requiresSvCheats: seed.requiresSvCheats,
  localOnly: seed.localOnly,
  safeForOfficialServers: seed.safeForOfficialServers,
  showInCompetitiveConfig: seed.showInCompetitiveConfig,
  showInPracticeConfig: seed.showInPracticeConfig,
  showInFunConfig: seed.showInFunConfig,
  showInTournamentSafeConfig: seed.showInTournamentSafeConfig,
  needsValidation: seed.needsValidation,
  warningLevel: seed.warningLevel,
  locales: localCopy(seed),
});

